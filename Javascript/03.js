// Arithmetic Operators in JS

// Addition
console.log(2 + 5);
// Substraction
console.log(2 - 5);
// Multiplication
console.log(2 * 5);
// Division
console.log(2 / 5);
// Modulous
console.log(2 % 5);
// Power
console.log(2 ** 5);

// increment and decrement in JS

let a = 1;

console.log(++a);

console.log(--a);

// Comparison Operators

a = 5;
b = 6;

console.log(a == b);
